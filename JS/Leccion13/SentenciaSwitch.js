let numero = 7;

let numerotexto = 'Valor desconocido';

switch( numero ){
    case 1 :
        numerotexto = 'Numero uno';
        break;
    case 2:
        numerotexto = 'Numero dos';
        break;
    case 3:
        numerotexto = 'Numero tres';
        break;
    case 4:
        numerotexto = 'Numero cuatro';
        break;
    default:
        numerotexto = 'Numero desconocido';


}
console.log(numerotexto);